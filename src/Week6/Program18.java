package Week6;

public class Program18 {
    public static void main(String[] args) {
        //addition,subtraction,multiplication,division and moderate of given numbers
        int a=125;//using integer datatype
        int b=24;
        System.out.println("addition of 125 +24 ="+(a+b));//149
        System.out.println("subtraction of 125-24 ="+(a-b));//101
        System.out.println("multiplication of 125*24 ="+(a*b));//3000
        System.out.println("division of 125/24 ="+(a/b));//5
        System.out.println("moderate of 125%24 ="+(a%b));//5
    }
}
